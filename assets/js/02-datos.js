/**
 * ¡Tipos de datos!
 *
 * En JavaScript, los datos pueden ser de los siguientes tipos:
 * String, Number, Boolean, Object, Array, Null, Undefined, Symbol,
 * BigInt y Function.
 *
 */

// String
let nombre = "Juan";
let apellido = "Pérez";

// Number
let edad = 30;
let precio = 150.5;

// Boolean
let activo = true;
let deshabilitado = false;

// Object
let persona = {
  nombre: "Juan",
  apellido: "Pérez",
  edad: 30,
};

// Array
let colores = ["Rojo", "Verde", "Azul"];

// Null
let nulo = null;

// Undefined
let indefinido = undefined;

// Symbol
let simbolo = Symbol("mi-simbolo");

// BigInt
let bigInt = 1234567890123456789012345678901234567890n;

// Function
let sumar = function (a, b) {
  return a + b;
};
